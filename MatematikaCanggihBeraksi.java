public class MatematikaCanggihBeraksi {
    private int a, b;
    
    public MatematikaCanggihBeraksi(){
        a = 1;
        b = 2;
    }
    public int tambah() {
        return a + b;
    }
    public int kali() {
        return a * b;
    }
    public int modulus() {
        return a % b;
    }
}
